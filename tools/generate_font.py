#!/usr/bin/env python3
"""
8x8 비트맵 폰트 생성기
ASCII 문자 0-255를 위한 기본 폰트 데이터를 생성합니다.
"""

def generate_basic_font():
    """기본 8x8 폰트 데이터 생성 (256 문자 x 8 바이트 = 2048 바이트)"""
    font_data = bytearray(2048)
    
    # 간단한 폰트 정의 (일부 문자만 정의)
    # 각 문자는 8x8 픽셀이며, 각 바이트는 한 행을 나타냅니다.
    
    # 공백 (0x20)
    font_data[0x20 * 8:0x20 * 8 + 8] = [
        0b00000000,
        0b00000000,
        0b00000000,
        0b00000000,
        0b00000000,
        0b00000000,
        0b00000000,
        0b00000000,
    ]
    
    # ! (0x21)
    font_data[0x21 * 8:0x21 * 8 + 8] = [
        0b00011000,
        0b00011000,
        0b00011000,
        0b00011000,
        0b00011000,
        0b00000000,
        0b00011000,
        0b00000000,
    ]
    
    # A (0x41)
    font_data[0x41 * 8:0x41 * 8 + 8] = [
        0b00111100,
        0b01100110,
        0b01100110,
        0b01111110,
        0b01100110,
        0b01100110,
        0b01100110,
        0b00000000,
    ]
    
    # B (0x42)
    font_data[0x42 * 8:0x42 * 8 + 8] = [
        0b01111100,
        0b01100110,
        0b01100110,
        0b01111100,
        0b01100110,
        0b01100110,
        0b01111100,
        0b00000000,
    ]
    
    # C (0x43)
    font_data[0x43 * 8:0x43 * 8 + 8] = [
        0b00111100,
        0b01100110,
        0b01100000,
        0b01100000,
        0b01100000,
        0b01100110,
        0b00111100,
        0b00000000,
    ]
    
    # D (0x44)
    font_data[0x44 * 8:0x44 * 8 + 8] = [
        0b01111000,
        0b01101100,
        0b01100110,
        0b01100110,
        0b01100110,
        0b01101100,
        0b01111000,
        0b00000000,
    ]
    
    # E (0x45)
    font_data[0x45 * 8:0x45 * 8 + 8] = [
        0b01111110,
        0b01100000,
        0b01100000,
        0b01111100,
        0b01100000,
        0b01100000,
        0b01111110,
        0b00000000,
    ]
    
    # F (0x46)
    font_data[0x46 * 8:0x46 * 8 + 8] = [
        0b01111110,
        0b01100000,
        0b01100000,
        0b01111100,
        0b01100000,
        0b01100000,
        0b01100000,
        0b00000000,
    ]
    
    # G (0x47)
    font_data[0x47 * 8:0x47 * 8 + 8] = [
        0b00111100,
        0b01100110,
        0b01100000,
        0b01101110,
        0b01100110,
        0b01100110,
        0b00111100,
        0b00000000,
    ]
    
    # H (0x48)
    font_data[0x48 * 8:0x48 * 8 + 8] = [
        0b01100110,
        0b01100110,
        0b01100110,
        0b01111110,
        0b01100110,
        0b01100110,
        0b01100110,
        0b00000000,
    ]
    
    # I (0x49)
    font_data[0x49 * 8:0x49 * 8 + 8] = [
        0b00111100,
        0b00011000,
        0b00011000,
        0b00011000,
        0b00011000,
        0b00011000,
        0b00111100,
        0b00000000,
    ]
    
    # O (0x4F)
    font_data[0x4F * 8:0x4F * 8 + 8] = [
        0b00111100,
        0b01100110,
        0b01100110,
        0b01100110,
        0b01100110,
        0b01100110,
        0b00111100,
        0b00000000,
    ]
    
    # S (0x53)
    font_data[0x53 * 8:0x53 * 8 + 8] = [
        0b00111100,
        0b01100110,
        0b01100000,
        0b00111100,
        0b00000110,
        0b01100110,
        0b00111100,
        0b00000000,
    ]
    
    # 0 (0x30)
    font_data[0x30 * 8:0x30 * 8 + 8] = [
        0b00111100,
        0b01100110,
        0b01101110,
        0b01110110,
        0b01100110,
        0b01100110,
        0b00111100,
        0b00000000,
    ]
    
    # 1 (0x31)
    font_data[0x31 * 8:0x31 * 8 + 8] = [
        0b00011000,
        0b00111000,
        0b00011000,
        0b00011000,
        0b00011000,
        0b00011000,
        0b01111110,
        0b00000000,
    ]
    
    # 나머지는 기본 패턴으로 채우기
    for i in range(256):
        # 이미 정의된 문자는 건너뜀
        if any(font_data[i * 8:i * 8 + 8]):
            continue
        # 간단한 박스 패턴
        font_data[i * 8:i * 8 + 8] = [
            0b01111110,
            0b01000010,
            0b01000010,
            0b01000010,
            0b01000010,
            0b01000010,
            0b01111110,
            0b00000000,
        ]
    
    return bytes(font_data)

if __name__ == "__main__":
    font_data = generate_basic_font()
    with open("assets/font8x8_basic.bin", "wb") as f:
        f.write(font_data)
    print(f"Generated font8x8_basic.bin ({len(font_data)} bytes)")

